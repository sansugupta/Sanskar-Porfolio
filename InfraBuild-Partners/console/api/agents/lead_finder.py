def find_leads(
    category: str,
    country: str = "USA",
    count: int = 30,
) -> dict:
    """
    Main entry point for lead finding.

    Args:
        category: Business category (restaurants, cafes, etc.)
        country: Target country
        count: Number of leads to find
    """
    if category not in BUSINESS_CATEGORIES:
        return {"error": f"Invalid category. Choose from: {', '.join(BUSINESS_CATEGORIES.keys())}"}

    search_query = BUSINESS_CATEGORIES[category]
    leads = search_google_leads(search_query, country, category, count)

    return {
        "success": True,
        "category": category,
        "country": country,
        "count": len(leads),
        "leads": leads,
        "timestamp": datetime.now().isoformat(),
    }
