def generate_excel(leads: list, category: str, country: str) -> BytesIO:
    """Generate a formatted Excel workbook from lead data."""
    wb = Workbook()
    ws = wb.active
    ws.title = f"{category.title()} Leads"

    # Header styling
    header_font = Font(name='Inter', bold=True, color='FFFFFF', size=11)
    header_fill = PatternFill(start_color='1a1a2e', end_color='1a1a2e', fill_type='solid')
    header_alignment = Alignment(horizontal='center', vertical='center')
    thin_border = Border(
        left=Side(style='thin', color='333355'),
        right=Side(style='thin', color='333355'),
        top=Side(style='thin', color='333355'),
        bottom=Side(style='thin', color='333355'),
    )
    data_font = Font(name='Inter', size=10)
    data_alignment = Alignment(vertical='center', wrap_text=True)

    headers = ['#', 'Business Name', 'Category', 'Phone', 'Email', 'Website',
               'Address', 'City', 'Country', 'Rating', 'Reviews', 'Source']

    for col, header in enumerate(headers, 1):
        cell = ws.cell(row=1, column=col, value=header)
        cell.font = header_font
        cell.fill = header_fill
        cell.alignment = header_alignment
        cell.border = thin_border

    # Column widths
    widths = [5, 28, 16, 18, 28, 32, 35, 16, 12, 8, 8, 20]
    for col, width in enumerate(widths, 1):
        ws.column_dimensions[ws.cell(row=1, column=col).column_letter].width = width

    # Data rows
    for idx, lead in enumerate(leads, 1):
        row_data = [
            idx,
            lead.get('name', 'N/A'),
            lead.get('category', category),
            lead.get('phone', 'N/A'),
            lead.get('email', 'N/A'),
            lead.get('website', 'N/A'),
            lead.get('address', 'N/A'),
            lead.get('city', 'N/A'),
            lead.get('country', country),
            lead.get('rating', 'N/A'),
            lead.get('reviews', 'N/A'),
            lead.get('source', 'Google'),
        ]
        for col, value in enumerate(row_data, 1):
            cell = ws.cell(row=idx + 1, column=col, value=value)
            cell.font = data_font
            cell.alignment = data_alignment
            cell.border = thin_border

    # Freeze header
    ws.freeze_panes = 'A2'
    ws.auto_filter.ref = ws.dimensions

    # Info sheet
    ws2 = wb.create_sheet('Info')
    ws2['A1'] = 'InfraBuild Partners — Lead Finder Report'
    ws2['A1'].font = Font(name='Inter', bold=True, size=14, color='1a1a2e')
    ws2['A3'] = f'Category: {category.title()}'
    ws2['A4'] = f'Country: {country}'
    ws2['A5'] = f'Total Leads: {len(leads)}'
    ws2['A6'] = f'Generated: {datetime.now().strftime("%Y-%m-%d %H:%M UTC")}'
    ws2['A7'] = 'Generated by InfraBuild Management Console'

    output = BytesIO()
    wb.save(output)
    output.seek(0)
    return output


